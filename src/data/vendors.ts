export function getVendorById(id: string): Vendor | undefined {
  return vendors.find(v => v.id === id);
}

export function getMedicinesByVendor(vendorId: string): MedicineProduct[] {
  return sampleMedicines.filter(m => m.vendor === vendorId);
}

export function searchMedicines(query: string): MedicineProduct[] {
  const lowerQuery = query.toLowerCase();
  return sampleMedicines.filter(m => 
    m.name.toLowerCase().includes(lowerQuery) ||
    m.genericName?.toLowerCase().includes(lowerQuery) ||
    m.manufacturer.toLowerCase().includes(lowerQuery)
  );
}
